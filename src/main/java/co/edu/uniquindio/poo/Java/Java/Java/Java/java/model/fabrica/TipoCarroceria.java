/**
 * Enum que representa los diferentes tipos de carrocería de un vehículo.
 * <ul>
 *   <li>SEDAN: Carrocería de tipo sedán, generalmente de cuatro puertas.</li>
 *   <li>COUPE: Carrocería de tipo coupé, usualmente de dos puertas y diseño deportivo.</li>
 *   <li>ROADSTER: Carrocería de tipo roadster, vehículo descapotable de dos plazas.</li>
 * </ul>
 */
public enum TipoCarroceria {

    SEDAN,COUPE,ROADSTER
}

